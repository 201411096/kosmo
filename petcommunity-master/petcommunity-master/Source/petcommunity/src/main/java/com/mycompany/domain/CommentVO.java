package com.mycompany.domain;

public class CommentVO {
	
	private String boardcommentId;
	private String boardcommentContent;
	private String boardcommentUploadtime;
	private String memberId;
	private String communityboardId;
	
	public String getBoardcommentId() {
		return boardcommentId;
	}
	public void setBoardcommentId(String boardcommentId) {
		this.boardcommentId = boardcommentId;
	}
	public String getBoardcommentContent() {
		return boardcommentContent;
	}
	public void setBoardcommentContent(String boardcommentContent) {
		this.boardcommentContent = boardcommentContent;
	}
	public String getBoardcommentUploadtime() {
		return boardcommentUploadtime;
	}
	public void setBoardcommentUploadtime(String boardcommentUploadtime) {
		this.boardcommentUploadtime = boardcommentUploadtime;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getCommunityboardId() {
		return communityboardId;
	}
	public void setCommunityboardId(String communityboardId) {
		this.communityboardId = communityboardId;
	}
	
	


}
